export function getProfile(id) {
  return (
    GROWTH_PROVIDER_PROFILES.find((profileItem) => profileItem.id === id) ??
    null
  );
}

export function getProfilesByCadence(cadence) {
  return GROWTH_PROVIDER_PROFILES.filter(
    (profileItem) => profileItem.cadence === cadence,
  );
}

export function summarizeRegistry(profiles = GROWTH_PROVIDER_PROFILES) {
  const byStatus = countBy(profiles, "status");
  const byProvider = countBy(profiles, "provider");
  const byPriority = countBy(profiles, "priority");
  const byCadence = countBy(profiles, "cadence");
  return {
    profile_count: profiles.length,
    by_status: byStatus,
    by_provider: byProvider,
    by_priority: byPriority,
    by_cadence: byCadence,
  };
}

export function selectDueProfiles({
  cadence = "weekly",
  includeApprovalRequired = false,
  includeNoDefault = false,
  includePlanned = true,
} = {}) {
  const cadenceOrder = cadenceRank(cadence);
  return GROWTH_PROVIDER_PROFILES.filter((profileItem) => {
    if (profileItem.cadence === "no_default" && !includeNoDefault) return false;
    if (profileItem.status === "excluded" && !includeNoDefault) return false;
    if (!includePlanned && profileItem.status === "planned") return false;
    if (!includeApprovalRequired && requiresApproval(profileItem)) return false;
    const profileRank = cadenceRank(profileItem.cadence);
    return Number.isFinite(profileRank) && profileRank <= cadenceOrder;
  });
}

export function requiresApproval(profileItem) {
  return (
    String(profileItem.approval ?? "").includes("required") ||
    profileItem.cadence === "on_approval"
  );
}

export function profileHealthSeed(profileItem) {
  if (profileItem.status === "excluded") return "WATCH";
  if (profileItem.status === "planned")
    return profileItem.priority === "P0" ? "BLOCKED" : "WATCH";
  if (requiresApproval(profileItem) && profileItem.status !== "implemented")
    return "WATCH";
  if (profileItem.status === "implemented") return "PASS";
  return "WATCH";
}

