function countBy(rows, key) {
  return rows.reduce((acc, row) => {
    const value = row[key] ?? "unknown";
    acc[value] = (acc[value] ?? 0) + 1;
    return acc;
  }, {});
}

function cadenceRank(cadence) {
  const ranks = {
    continuous: 0,
    daily: 1,
    weekly: 7,
    biweekly: 14,
    monthly: 30,
    quarterly: 90,
    on_approval: Number.POSITIVE_INFINITY,
    no_default: Number.POSITIVE_INFINITY,
  };
  return ranks[cadence] ?? Number.POSITIVE_INFINITY;
}

